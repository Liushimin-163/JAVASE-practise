public class Student {
    private String name;
    private String ab;

    public Student(String name, String ab) {
        this.name = name;
        this.ab = ab;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAb() {
        return ab;
    }

    public void setAb(String ab) {
        this.ab = ab;
    }
}
